import java.util.*;
public class ol_4036_Sum_and_Sub {
	public static void main(String args[]) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int m = sc.nextInt();
		
		int a =0; 
		int b =0;
		
		a= (n+m)/2;
		b= (n-m)/2;
		
		System.out.println(a);
		System.out.println(b);
		
	}//main
}
